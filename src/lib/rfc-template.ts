/** Default RFC body — the user can edit it before submitting. Designed to nudge
 * authors toward the four sections most engineering RFCs need. */
export const DEFAULT_RFC_TEMPLATE = `## Background

What's the current state of the world? What's the issue or opportunity that makes this RFC worth writing?

## Proposal

What do you propose we do, concretely? Be specific about the change, scope, and who it affects.

## Alternatives considered

What else did you consider? Why did you decide against them?

## Open questions

What's still uncertain? Where do you need input from reviewers before this can move forward?
`;
